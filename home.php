        <p>Di Indonesia, Penyakit DBD masih menjadi masalah untuk kebanyakan masyarakat Indonesia. Kekhawatiran akan terjangkitnya penyakit yang berasal dari gigitan nyamuk ini semakin bertambah saat musim penghujan tiba. Deteksi dini dan penanganan yang terlambat mengakibatkan meningkatnya kasus yang berakhir dengan kematian. 
        <br></a> Demam berdarah dengue (DBD) adalah penyakit demam serius yang ditularkan oleh nyamuk betina Aedes aegypti yang menyerang sistem peredaran darah manusia.</br>
            
        <br><p>Demam berdarah dengue bisa menjangkiti seseorang yang telah mendapat gigitan dari nyamuk betina Aedes aegypti. Nyamuk tersebut membawa virus pemicu yang berasal dari keluarga Flaviviridae, dengan empat jenis virus yang dikenal dengan serotipe (DENV-1, DENV-2, DENV-3, dan DENV-4).
             Serotipe tersebut bisa masuk ke dalam tubuh manusia melalui gigitan nyamuk. Virus kemudian mengambil alih mekanisme sel-sel tubuh seseorang. Serotipe demam berdarah dengue bekerja dengan membentuk komponen protein baru untuk memperbanyak diri.</p>  
        

    
