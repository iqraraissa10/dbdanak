        Jenis demam berdarah ini seringkali berakibat fatal pada anak-anak. Kuncinya, jangan segera menghentikan pengobatan DBD saat demam anak turun dan suhu tubuhnya sudah normal. Ini justru menandakan anak sedang dalam fase kritis. 
