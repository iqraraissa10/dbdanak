            <h2 class="art-postheader">
            <img src="images/makan.jpeg" width="60" height="60" alt="" />Nafsu Makan Turun Saat DBD?</h2>
        <div class="art-postcontent">



        <p><marquee scrolldelay="15"><strong><em>Welcome to website sistem pakar diagnosis Demam Berdarah Dangue (DBD) pada Anak</em></strong></marquee></p>
        <p><img style="border:0px solid #999; border-radius:5px 15px 5px 5px; float:left; -moz-box-shadow: 0 0 5px 5px #888;
           -webkit-box-shadow: 0 0 0px 0px#888;  box-shadow: 0 0 0px 0px #333;" width="250" src="images/bunda-begini-trik-agar-anak-mau-makan-sayur.jpg" alt="an image" />
        <br><p>Salah satu gejala penyakit DBD yang umum muncul adalah hilangnya nafsu makan. Nafsu makan yang turun ini sebaiknya jangan dianggap sepele karena bisa mengarah pada kondisi penurunan berat badan hingga malnutrisi. Keadaan ini dapat memperlambat proses penyembuhan serta pemulihan penyakit demam berdarah. Karenanya, meningkatkan nafsu makan penderita DBD menjadi hal yang penting untuk dilakukan.
          
        

       
        <br><br><br><br>
        <p align="justify"><h3><font color="#FF6633">Tips mengembalikan nafsu makan penderita DBD</font></h3></p>   
        <p> Meski tidak nafsu makan, penderita DBD harus tetap berusaha memenuhi asupan nutrisi per harinya. 
          Kenapa begitu? Karena kekurangan gizi akan menghambat pembentukan serta kerja trombosit alias keping darah. Selain itu, fungsi sistem imun tubuh dalam melawan virus demam berdarah juga ikut menurun. </p>
      	<p align="justify"><font color="#FF0000"><strong>
          <dl>1. Memakan makanan favorit</dl></strong></font></p>
          <ul>
          <li>
        <div align="justify">Asalkan bukan makanan pantangan seperti makanan manis dan pedas atau minuman bersoda, cara ini cukup ampuh untuk mengembalikan nafsu makan pasien DBD.</div>
          </li> 
          </ul>
            
          
        
        <p align="justify"><font color="#FF0000"><strong><dl>2. Makan sering tetapi dengan porsi kecil</dl></strong></font></p>
           <ul>
           <li>
        <div align="justify">Pasien DBD yang mengalami penurunan nafsu makan dapat membagi 3 porsi makan besar menjadi 5-6 porsi yang lebih kecil dan diberikan setiap kurang lebih dua jam sekali.</div>
           </li> </ul>



        <p align="justify"><font color="#FF0000"><strong><dl>3. Akali dengan minuman</dl></strong></font></p>
          <ul>
          <li>
        <div align="justify">Nutrisi penting yang dibutuhkan tubuh juga bisa didapatkan dari minuman. Tidak hanya membantu melengkapi kebutuhan gizi saja, minuman juga bisa mencegah dehidrasi pada pasien DBD. Banyak minum air pun dapat menggantikan cairan yang hilang karena demam dan muntah.</div>
          </li></ul>



        <p align="justify"><font color="#FF0000"><strong><dl>4. Hindari makanan dengan bau kuat dan berwarna pekat</dl></strong></font></p>
          <ul>
          <li>      
        <div align="justify">Hindari konsumsi makanan berbau kuat seperti keju. Disamping itu, hindari juga makanan dan minuman berwarna merah atau cokelat karena menyerupai warna darah. Hal ini dapat membuat Anda tidak nafsu makan.</div>
          </li>
          </ul>


