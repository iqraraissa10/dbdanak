			<h3><strong>Kasus DBD Meningkat ? Kok bisa ?</strong></h3>
			<div dir="ltr" trbidi="on">
			<p> Salah satu strategi pengendalian DBD yang paling direkomendasikan terutama kaitannya dengan pemberdayaan masyarakat adalah Pemberantasan Sarang Nyamuk 3M Plus melalui Gerakan 1 Rumah 1 Jumantik (1R1J). 
				Gerakan 1R1J merupakan gerakan yang bersifat nasional. Gerakan ini mengandung arti bahwa setiap bangunan harus memiliki 1 (satu) orang pengawas dalam kegiatan pemberantasan sarang nyamuk 3M Plus. <br />
			<br>
			<center>
			<img src="images/jumantik.jpeg" width="500" height="300" /></br>
			</center>
			<br/>Pada Gerakan 1 Rumah 1 Jumantik (1R1J), tugas seorang Jumantik atau Juru Pemantau Jentik bertugas untuk melakukan sosialisasi PSN 3M Plus kepada seluruh anggota keluarga/penghuni bangunan; memeriksa/memantau tempat perindukan nyamuk di dalam dan di luar rumah paling lambat  seminggu sekali; hasil pemantauan jentik dan pelaksanaan PSN 3 M Plus dicatat pada kartu jentik.<br /> </p>					
				
			
			
			<h3><strong>Awal Juni, Terjadi Penambahan 30 Kasus DBD di Jakarta Selatan ?</strong></h3>
			<div dir="ltr" trbidi="on">
			<p> Untuk pencegahan penyakit DBD, mengingatkan warga memperhatikan tempat penampungan air yang biasa menjadi perkembangbiakan nyamuk seperti bak mandi/ember, ban bekas, pelepah daun yang menampung air, tatakan pot tanaman, tatakan dispenser, dan sebagainya. Sebagai tambahan, Fitria mengimbau masyarakat melakukan gerakan "3M" plus, yakni Menguras, Menutup, dan Mengubur.
