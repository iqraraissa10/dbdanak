	<h3><strong>DBD ?</strong></h3>
    <div dir="ltr" trbidi="on">
            <p>Demam Berdarah Dengue adalah penyakit yang ditularkan oleh nyamuk yang terjadi di daerah tropis dan subtropis di dunia. Untuk demam berdarah ringan, maka ia akan menyebabkan demam tinggi dan gejala seperti flu. Sementara untuk demam berdarah yang parah, ia bisa menyebabkan pendarahan serius, 
				penurunan tekanan darah secara tiba-tiba (syok) dan bahkan kematian. <br />
			<br/>Peningingkatan kasus DBD terus terjadi terutama saat musim hujan. Kementerian Kesehatan mencatat di tahun 2022, jumlah kumulatif kasus Dengue di Indonesia sampai dengan Minggu ke-22 dilaporkan 45.387 kasus. Sementara jumlah kematian akibat DBD mencapai 432 kasus.<br />                                    								
			<br/>Kini para peneliti sedang mengupayakan untuk membuat vaksin demam berdarah. Untuk saat ini, di daerah di mana demam berdarah biasa terjadi, 
				cara terbaik untuk mencegah infeksi adalah dengan menghindari gigitan nyamuk dan mengambil langkah-langkah yang perlu dilakukan untuk mengurangi populasi nyamuk.<br />							
