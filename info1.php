    <p>Tahapan berikutnya dari DBD pada anak adalah dengue syok syndrome atau DSS. Ini merupakan jenis demam berdarah yang paling buruk dan fatal. Saat anak mengalami demam berdarah ini, gejala yang muncul adalah gejala demam dengue dan DBD. Lalu, ada lagi gejala DSS yang berupa:</p>
	<ul>
    <li>
      <div align="justify">1. Perdarahan yang terjadi secara tiba-tiba dan tidak berhenti pada area tubuh mana saja.</div>
      <div align="justify">2. Tekanan darah menurun secara drastis yang mengakibatkan kesadaran anak pun menurun dengan cepat.</div>
      <div align="justify">3. Terdapat kebocoran pada bagian pembuluh darah.</div>
      <div align="justify">4. Jumlah trombosit mengalami penurunan </div>
      <div align="justify">5. Denyut nadi melemah. </div>
      </li>
  </ul>
