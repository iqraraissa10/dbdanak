        <p> Ciri-ciri lain dari nyamuk demam berdarah adalah kebiasaannya bertelur. Mereka biasanya menaruh telurnya di wadah berisi air di sekitaran rumah. Termasuk di tempat-tempat yang tidak terpakai seperti bekas botol, ban hingga sampah lain yang bisa menampung air. </p> <p>Ciri-ciri nyamuk demam berdarah yang tidak boleh kamu anggap remeh adalah masa hidupnya yang panjang karena biasanya nyamuk ini beristirahat di tempat gelap (mulai dari lemari, kolong tempat tidur hingga di balik tirai) sehingga jauh dari predator.</p> 

  
    <br><br><br>
	<h3><p><strong>Gejala Demam Berdarah Dengue (DBD) pada Anak</strong></p></h3>
    <img style="border:0px solid #999; border-radius:5px 15px 5px 5px; float:left; -moz-box-shadow: 0 0 0px 5px #888;
	  -webkit-box-shadow: 0 0 5px 5px#888;  box-shadow: 0 0 2px 0px #333;" width="289" src="../dbdanak/images/dbd6.jpg" alt="an image" /> 
	
    <p>Gejala yang muncul pada anak biasanya dimulai antara 24 sampai 48 jam setelah penurunan suhu tubuh mulai terjadi. Beberapa gejalanya, antara lain: </p>
	<ul>
        <li>
        <div align="justify">1. Mengalami sakit perut atau perut terasa nyeri ketika ditekan</div>
        <div align="justify">2. Perubahan suhu tubuh secara drastis, dari demam menjadi hipotermia</div>
        <div align="justify">3. Muntah terus</div>
        <div align="justify">4. Muntah darah atau mengalami feses berdarah</div>
        <div align="justify">5. Anak mengalami mimisan</div>
        <div align="justify">6. Gusi berdarah tanpa sebab.</div>
        </li>
        <p>&nbsp;</p>
    </ul>

        <br>
        <h3><p><strong>Gejala Demam Berdarah pada Anak Disertai Syok</strong></p></h3>
